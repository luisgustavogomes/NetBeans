    private void btnExcluirLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirLivroActionPerformed
        if (tabelaLivros.getSelectedRow() > 0) {
            Livro livro = new Livro(retornaStringLinhaColuna(rowIndex(), 0), retornaStringLinhaColuna(rowIndex(), 1), Integer.parseInt(retornaStringLinhaColuna(rowIndex(), 2)), retornaStringLinhaColuna(rowIndex(), 3), retornaStringLinhaColuna(rowIndex(), 4));
            ((DefaultTableModel) tabelaLivros.getModel()).removeRow(tabelaLivros.getSelectedRow());
            boolean status = GerenciadorBibliotecaVirtual.isExclusaoLivro(livro);
            if (status) {
                carregarTabela();
                limparCampos();
                JOptionPane.showMessageDialog(this, "Livro excluido com sucesso!");
            }
        } else {
            carregarTabela();
            limparCampos();
            JOptionPane.showMessageDialog(this, "Erro!");
        }
    }//GEN-LAST:event_btnExcluirLivroActionPerformed

    private void btnSalvarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarLivroActionPerformed
        Livro livroAnterior = new Livro(retornaStringLinhaColuna(rowIndex(), 0), retornaStringLinhaColuna(rowIndex(), 1), Integer.parseInt(retornaStringLinhaColuna(rowIndex(), 2)), retornaStringLinhaColuna(rowIndex(), 3), retornaStringLinhaColuna(rowIndex(), 4));
        Livro livroNovo = new Livro(txtNomeLivro.getText(), txtAutorLivro.getText(), Integer.parseInt(txtNumeroPaginasLivro.getText()), txtEditoraLivro.getText(), txtConteudoLivro.getText());
        if (!livroNovo.equals(livroAnterior)) {
            novoLivro(0);
            GerenciadorBibliotecaVirtual.isExclusaoLivro(livroAnterior);
            limparCampos();
            carregarTabela();
            JOptionPane.showMessageDialog(this, "Livro alterado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro!");
        }
    }//GEN-LAST:event_btnSalvarLivroActionPerformed


