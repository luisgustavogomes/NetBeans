    public void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaLivros.getModel();
        String lista[] = new String[5];
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        GerenciadorBibliotecaVirtual.livros.forEach((Livro l) -> {
            lista[0] = l.getNome();
            lista[1] = l.getAutor();
            lista[2] = String.valueOf(l.getNumeroPaginas());
            lista[3] = l.getEditora();
            lista[4] = l.getConteudo();
            modelo.addRow(lista);
        });

    }

    private void limparCampos() {
        txtNomeLivro.setText("");
        txtAutorLivro.setText("");
        txtEditoraLivro.setText("");
        txtNumeroPaginasLivro.setText("");
        txtConteudoLivro.setText("");
    }

    private int rowIndex() {
        return tabelaLivros.getSelectedRow();
    }

    /**
     * Retorna a String contida na matriz de Jtable, passando linha e coluna
     * como parametro.
     *
     * @param linha
     * @param coluna
     * @return
     */
    private String retornaStringLinhaColuna(int linha, int coluna) {
        try {
            return tabelaLivros.getValueAt(linha, coluna).toString();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Não encontrado!");
            return null;
        }
    } 

    /**
     * Novo Livro Para ativar o JOptionPane passe o valor 1 para sim e 2 não
     *
     * @param controlador 1 = Sim 0 = não
     */
    private void novoLivro(int controlador) {
        if (txtNomeLivro.getText().isEmpty() || txtAutorLivro.getText().isEmpty() || txtNumeroPaginasLivro.getText().isEmpty()
                || txtEditoraLivro.getText().isEmpty() || txtConteudoLivro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Erro, não foi possivel cadastrar livro!\nPreencha todos os campos!");
        } else {
            boolean status = GerenciadorBibliotecaVirtual.isCadastrarArquivoLivro(txtNomeLivro.getText(), txtAutorLivro.getText(), Integer.parseInt(txtNumeroPaginasLivro.getText()), txtEditoraLivro.getText(), txtConteudoLivro.getText());
            if (status) {
                limparCampos();
                carregarTabela();
                switch (controlador) {
                    case 1:
                        JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso!");
                }
            }
        }
    }

    private void excluirLivro(Livro livro) {
        GerenciadorBibliotecaVirtual.isExclusaoLivro(livro);
    }

