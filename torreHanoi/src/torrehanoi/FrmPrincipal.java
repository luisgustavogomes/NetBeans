    /**
     * Contralodor de mensagens
     *
     * @param controlador Escolha 1 para erro checkBox, 2 para erro subreposição
     */
    private void erro(int controlador) {
        switch (controlador) {
            case 1:
                JOptionPane.showMessageDialog(this, "Por favor, selecionar somente duas opções","Erro", JOptionPane.ERROR_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "Erro de sobreposição!","Erro", JOptionPane.ERROR_MESSAGE);
                break;
        }

    }

    private void trocar(JButton[] bDestino, JButton[] bOrigem, Pilha pDestino, Pilha pOrigem) {
        if (pOrigem.getTamanho() > 0) {
            int valor = pOrigem.pop();
            pDestino.push(valor);
            bDestino[pDestino.getTamanho() - 1].setText(String.valueOf(valor));
            bOrigem[pOrigem.getTamanho()].setText(" ");
        }
    }

    private boolean isVerificador(Pilha pDestino, Pilha pOrigem) {
        int d = pDestino.top();
        int o = pOrigem.top();
        return pDestino.top() > pOrigem.top();
    }

