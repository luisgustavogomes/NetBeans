    private void btnTrocaDireitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrocaDireitaActionPerformed
        if (!isVerificadorCheckBox()) {
            if (cbX.isSelected() && cbY.isSelected()) {
                if (isVerificador(Y, X)) {
                    trocar(botoesY, botoesX, Y, X);
                } else {
                    erro(2);
                }
            } else if (cbX.isSelected() && cbZ.isSelected()) {
                if (isVerificador(Z, X)) {
                    trocar(botoesZ, botoesX, Z, X);
                } else {
                    erro(2);
                }
            } else if (cbY.isSelected() && cbZ.isSelected()) {
                if (isVerificador(Z, Y)) {
                    trocar(botoesZ, botoesY, Z, Y);
                } else {
                    erro(2);
                }
            }
        } else {
            erro(1);
        }
    }//GEN-LAST:event_btnTrocaDireitaActionPerformed

